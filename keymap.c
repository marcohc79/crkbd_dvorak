bool oled_task_user(void) {
    if (is_keyboard_master()) {
        render_space(); // Call this to render the space stuff on the one screen
    } else {
        oled_render_logo();
    }
    return false;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {

        mod_state = get_mods();

        switch (keycode) {

                case LT(_SYM, KC_CAPS):

                        if (record->tap.count && record->event.pressed) {
                                tap_code16(KC_CAPS);
                        } else if (record->event.pressed) {
                                layer_on(_SYM);
                                update_tri_layer(_SYM, _NUM, _MEDIA);
                        } else {
                                layer_off(_SYM);
                                update_tri_layer(_SYM, _NUM, _MEDIA);
                        }
                        return false;

                case LT(_NUM, KC_ENT):
                        if (record->tap.count && record->event.pressed) {
                                tap_code16(KC_ENT);
                        } else if (record->event.pressed) {
                                layer_on(_NUM);
                                update_tri_layer(_SYM, _NUM, _MEDIA);
                        } else {
                                layer_off(_NUM);
                                update_tri_layer(_SYM, _NUM, _MEDIA);
                        }
                        return false;
        }
  return true;
}
